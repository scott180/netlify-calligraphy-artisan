    <h1>无为徐生</h1>

    <p>书法练习轨迹--明月几时有</p>

    <p>何以学书法，一字记之曰：练。天天练，月月练，年年练。时时琢磨。</p>

    <p>人情必有所寄，习惯而成自然。练着练着，书法就成了生活的一部分，记录书法练习轨迹更是有趣有意思的点缀。不能说是点缀，于我而言，练字与记录是相辅相成、缺一不可的关系。</p>

